class Person():
    def __init__(self, id, name, org_id ):
        self.id = id
        self.name = name
        self.org_id = org_id

    def get_id(self):
        return self.id

    def set_id(self, id):
        self.id = id

    def get_name(self):
        return self.name

    def set_name(self, name):
        self.name = name

    def get_org_id(self):
        return self.org_id

    def set_id(self, org_id):
        self.org_id = org_id

